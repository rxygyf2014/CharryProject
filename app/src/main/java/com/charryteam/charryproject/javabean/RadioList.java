package com.charryteam.charryproject.javabean;

import com.lidroid.xutils.db.annotation.Column;
import com.lidroid.xutils.db.annotation.Id;
import com.lidroid.xutils.db.annotation.Table;

/**
 * Created by xiabaikui on 2015/12/11.
 * "artist": "测试小子",
 * "artist_id": 1001,
 * "music_id": "103171",
 * "music_name": "117.我喜欢你，但是我更喜欢自己",
 * "music_type": 1
 */
@Table(name = "RadioList")
public class RadioList {
    @Column(column = "artist")
    @Id(column = "_id")
    private int _id;

    @Column(column = "artist")
    private String artist;

    @Column(column = "artist_id")
    private String artist_id;

    @Column(column = "music_id")
    private String music_id;

    @Column(column = "music_name")
    private String music_name;

    @Column(column = "music_type")
    private String music_type;

    public int get_id() {
        return _id;
    }

    public void set_id(int _id) {
        this._id = _id;
    }

    public String getArtist() {
        return artist;
    }

    public void setArtist(String artist) {
        this.artist = artist;
    }

    public String getArtist_id() {
        return artist_id;
    }

    public void setArtist_id(String artist_id) {
        this.artist_id = artist_id;
    }

    public String getMusic_id() {
        return music_id;
    }

    public void setMusic_id(String music_id) {
        this.music_id = music_id;
    }

    public String getMusic_name() {
        return music_name;
    }

    public void setMusic_name(String music_name) {
        this.music_name = music_name;
    }

    public String getMusic_type() {
        return music_type;
    }

    public void setMusic_type(String music_type) {
        this.music_type = music_type;
    }

    @Override
    public String toString() {
        return "RadioList{" +
                "_id=" + _id +
                ", artist='" + artist + '\'' +
                ", artist_id='" + artist_id + '\'' +
                ", music_id='" + music_id + '\'' +
                ", music_name='" + music_name + '\'' +
                ", music_type='" + music_type + '\'' +
                '}';
    }
}
