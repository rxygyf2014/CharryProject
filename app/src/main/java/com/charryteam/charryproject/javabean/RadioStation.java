package com.charryteam.charryproject.javabean;

import com.lidroid.xutils.db.annotation.Column;
import com.lidroid.xutils.db.annotation.Id;
import com.lidroid.xutils.db.annotation.Table;

/**
 * Created by xiabaikui on 2015/12/6.
 */
@Table(name = "radioStation")
public class RadioStation {

    @Column(column = "id")
    @Id(column ="_id")
    private int id;

    @Column(column = "radio_id")

    private int radio_id;

    @Column(column = "radio_type")
    private int radio_type;

    @Column(column = "listen_count")
    private int listen_count;

    @Column(column = "nick")
    private String nick;

    @Column(column = "radio_name")
    private String radio_name;

    @Column(column = "img")
    private String img;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getRadio_id() {
        return radio_id;
    }

    public void setRadio_id(int radio_id) {
        this.radio_id = radio_id;
    }

    public int getRadio_type() {
        return radio_type;
    }

    public void setRadio_type(int radio_type) {
        this.radio_type = radio_type;
    }

    public int getListen_count() {
        return listen_count;
    }

    public void setListen_count(int listen_count) {
        this.listen_count = listen_count;
    }

    public String getNick() {
        return nick;
    }

    public void setNick(String nick) {
        this.nick = nick;
    }

    public String getRadio_name() {
        return radio_name;
    }

    public void setRadio_name(String radio_name) {
        this.radio_name = radio_name;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    @Override
    public String toString() {
        return "RadioStation{" +
                "id=" + id +
                ", radio_id=" + radio_id +
                ", radio_type=" + radio_type +
                ", listen_count=" + listen_count +
                ", nick='" + nick + '\'' +
                ", radio_name='" + radio_name + '\'' +
                ", img='" + img + '\'' +
                '}';
    }
}
